// ─── TRANSLATION FUNCTION ─────────────────────────────────────────────────────

/**
 * Get a translated string. Falls back to English if key not found in current language.
 * Supports interpolation: t("hello", { name: "World" }) with "hello": "Hello {name}"
 */
export function t(key, params = {}) {
  const lang = _currentLang;
  let str = translations[lang]?.[key] || translations.en[key] || key;

  // Interpolate params: {name} → value
  for (const [k, v] of Object.entries(params)) {
    str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
  }

  // Handle plural {s} — shows "s" if count !== 1
  if (params.count !== undefined) {
    str = str.replace(/\{s\}/g, params.count === 1 ? "" : "s");
  }

  return str;
}

