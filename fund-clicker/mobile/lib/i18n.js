import { Platform, NativeModules } from "react-native";

// ─── LOCALE DETECTION ─────────────────────────────────────────────────────────

function getDeviceLocale() {
  try {
    if (Platform.OS === "web") {
      return (typeof navigator !== "undefined" && (navigator.language || navigator.languages?.[0])) || "en";
    }
    if (Platform.OS === "ios") {
      return (
        NativeModules.SettingsManager?.settings?.AppleLocale ||
        NativeModules.SettingsManager?.settings?.AppleLanguages?.[0] ||
        "en"
      );
    }
    return NativeModules.I18nManager?.localeIdentifier || "en";
  } catch {
    return "en";
  }
}

function getLanguageCode(locale) {
  return (locale || "en").split(/[-_]/)[0].toLowerCase();
}

let _currentLang = getLanguageCode(getDeviceLocale());

export function setLanguage(lang) { _currentLang = lang; }
export function getLanguage() { return _currentLang; }

